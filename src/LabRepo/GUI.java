    void refrescarArbol(){
       DefaultTreeModel tm = (DefaultTreeModel) jtArbol.getModel();
       DefaultMutableTreeNode root = (DefaultMutableTreeNode) tm.getRoot();
       
        for (int i = 0; i < root.getChildCount(); i++) {
           DefaultMutableTreeNode nodo = (DefaultMutableTreeNode) root.getChildAt(i);
            if (nodo.getChildCount() >  0 && (nodo.getUserObject() instanceof Padre) == false) {
                DefaultMutableTreeNode nodo2 = new DefaultMutableTreeNode((Padre) nodo.getUserObject());
                
                for (int j = 0; j < nodo.getChildCount(); j++) {
                    nodo2.add(new DefaultMutableTreeNode(nodo.getChildAt(i)));
                }
            }
        }
    }

